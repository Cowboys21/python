# Items in a list are identified by their position
# in the list, starting with zero
# The number in parentheses is called the index of the item. Because lists start at zero, the index
# of an item is always one less than its position
family = ['Aarshabh', 'mama','papa']
fam = family[2]
print(fam.title())
lovely = family[-2]
print(lovely.title())
